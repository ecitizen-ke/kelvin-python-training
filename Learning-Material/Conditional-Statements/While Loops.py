# Initialize the variable count with value 1
count = 1

# Start a while loop that runs as long as count is less than 6
while count < 6:
    # Print the current value of count
    print(f"The current count is {count}")
    # Increment the value of count by 1
    count += 1
else:
    # When the condition of the while loop becomes False, execute this block
    # Print a message indicating that count is not less than 6
    print(f"Current count: {count} is greater than 6")
