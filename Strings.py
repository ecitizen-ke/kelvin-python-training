# Define a string
my_string = "Have a wonderful day okay?"

# Count the occurrences of 'a' in the string
count = my_string.count('a')
print(count)

# Replace all occurrences of 'a' with 'z' in the string
new_string = my_string.replace('a', 'z')
print(new_string)

# Prompt the user to input their first name and store it in the firstName variable
firstName = input("Enter first name: ")

# Prompt the user to input their last name and store it in the lastName variable
lastName = input("Enter last name: ")

# Print a welcome message using the user's first and last name
print(f"Hello {firstName} {lastName}! There, have a wonderful day!!")

