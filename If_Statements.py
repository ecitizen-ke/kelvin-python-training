# Define a list of fruits
fruits = ["apple", "banana", "watermelon"]

# Define a variable for the requested fruit
requested_fruit = "banana"

# Check if the requested fruit is in the list of fruits
if requested_fruit in fruits:
    print(f"Yes, '{requested_fruit}' is in the list of fruits.")
else:
    print(f"No, '{requested_fruit}' is not in the list of fruits.")
